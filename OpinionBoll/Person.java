package OpinionBoll;

public class Person {
    //характеристики-->полета
    private String name;
    private int age;
    //конструктор-->създавам обекта
    public Person(String name,int age){
        this.name=name;
        this.age=age;
    }
    //функционалности-->методи
    public String getName(){
        return this.name=name;
    }
    public int getAge(){
        return this.age=age;
    }

}
